public class Animal
{
   String name;
   int age;
   double size;
   String food;
   
   void setName(String str)
   {
      name = str;
   }
   
   String getName()
   {
      return name;
   }
   
   void setAge(int num)
   {
      age = num;
   }
   
   int getAge()
   {
      return age;
   }
   
   void setSize(double num)
   {
      size = num;
   }
   
   double getSize()
   {
      return size;
   }
   
   void setFood(String str)
   {
      food = str;
   }
   
   String getFood()
   {
      return food;
   }
}
